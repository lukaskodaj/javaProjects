package hadik;

public class Telo {

    private int x;
    private int y;

    public Telo(int x, int y) {
        this.x = x;
        this.y = y;
    }

    /* getter pre suradnicu X */
    public int getX() {
        return this.x;
    }

    /* setter pre suradnicu X */
    public void setX(int x) {
        this.x = x;
    }

    /* getter pre suradnicu Y */
    public int getY() {
        return this.y;
    }

    /* setter pre suradnicu Y */
    public void setY(int y) {
        this.y = y;
    }

}
